//! # 🔄 Cycle — Ciclo Fechado SIL
//!
//! O ciclo fechado é fundamental no padrão SIL:
//!
//! > *"L(F) → L(0) fecha o loop"*
//!
//! ## Estrutura do Ciclo
//!
//! ```text
//! ┌──────────────────────────────────────┐
//! │           CICLO SIL                  │
//! ├──────────────────────────────────────┤
//! │                                      │
//! │  ┌─────────────┐                    │
//! │  │ PERCEBER    │ ← L(0-4)           │
//! │  └──────┬──────┘                    │
//! │         ▼                           │
//! │  ┌─────────────┐                    │
//! │  │ PROCESSAR   │ ← L(5-7)           │
//! │  └──────┬──────┘                    │
//! │         ▼                           │
//! │  ┌─────────────┐                    │
//! │  │ INTERAGIR   │ ← L(8-A)           │
//! │  └──────┬──────┘                    │
//! │         ▼                           │
//! │  ┌─────────────┐                    │
//! │  │ EMERGIR     │ ← L(B-C)           │
//! │  └──────┬──────┘                    │
//! │         ▼                           │
//! │  ┌─────────────┐                    │
//! │  │ META        │ ← L(D-F)           │
//! │  └──────┬──────┘                    │
//! │         │                           │
//! │         └────────► L(F) → L(0)      │
//! │                                      │
//! └──────────────────────────────────────┘
//! ```

